////////////////////////////////////////////////////////////////////////////////
#ifndef XMLERROR_HPP
#define XMLERROR_HPP

////////////////////////////////////////////////////////////////////////////////
#include <QString>
#include <utility>

////////////////////////////////////////////////////////////////////////////////
class XmlError
{
public:
    XmlError(QString message) : _message(std::move(message)) { }
    ~XmlError() { }

    virtual const QString& message() const noexcept { return _message; }

protected:
    QString _message;
};

////////////////////////////////////////////////////////////////////////////////
#endif // XMLERROR_HPP
